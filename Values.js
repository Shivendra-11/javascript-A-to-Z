// Numbers
console.log(22);    // Integer number
console.log(2.4);   // Floating point number
console.log(-3);    // Negative integer
console.log(0);
console.log(-0);

// Boolean
console.log(false); // Boolean value false
console.log(true);  // Boolean value true

// null and undefined
let y ;
console.log(y); // Output: null
// console.log(null);       // A null value
// console.log(undefined);  // Undefined value (variable has not been assigned a value)
let p = null;
console.log(p); // Output: null

// Strings
console.log("hello");   // Double-quoted string
console.log('hello');   // Single-quoted string
console.log(`hello`);   // Template literal string


// Type of operator

console.log(typeof 6.3) //number
console.log(typeof "number"); //String
console.log(typeof true) //boolean

console.log(typeof undefined); //undefined
console.log(typeof null); //object //corner case
console.log(typeof NaN) //it is a number
console.log(typeof(typeof(233))); //string
 